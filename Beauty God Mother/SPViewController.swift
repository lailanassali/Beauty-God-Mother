import UIKit
import Firebase
import FirebaseDatabase

class SPViewController: UIViewController {
    
    
    @IBOutlet weak var organisation: UITextField!
    @IBOutlet weak var username: UITextField!
    @IBOutlet weak var email: UITextField!
    @IBOutlet weak var password: UITextField!
    @IBOutlet weak var confirmPassword: UITextField!
    
    var rs = RegistrationService.shared
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }
    
  
    @IBAction func registerPressed(_ sender: Any) {
        handleRegister()
        
    }
    
    
    public func handleRegister() {
        // stage 1
        guard let organisation = organisation.text, let username = username.text, let email = email.text, let password = password.text, let confirmPassword = confirmPassword.text else {return}
        
        // stage 2
        if !rs.passwordMatches(p1: password, p2: confirmPassword) && !rs.nameFormatIsCorrect(name: organisation)
            && !rs.usernameFormatIsCorrect(username: username) { return }
        
        
        Auth.auth().createUser(withEmail: email, password: password) { (result:AuthDataResult?, error) in
            if let error = error {
                print("Failed to create User:",error)
                return
            }
            
            //             Save id generated by Firebase
            guard let uid = result?.user.uid else {return}
            
            // Database Structure
            let dictionaryValues = [
                                "organisation": organisation,
                                "username": username,
                                    "email": email]
            // Save values to uid
            let values = [uid:dictionaryValues]
            Database.database().reference().child("providers").updateChildValues(values, withCompletionBlock: { (err, ref) in
                if let err = error {
                    print("Failed to save user to database:",err)
                    return
                }
                
                print("Sucessfully added \(username) to database!")
                self.performSegue(withIdentifier: "sellerToConfirmation", sender: self)
            })
        }
        
        
    }
    
    func goToNextViewController() {
        
    }
    
}
        

